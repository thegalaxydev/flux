#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub enum IconSize {
    S14,
    S16,
    S18,
    S20,
    S24,
    S32,
}

impl IconSize {
    pub fn px(self) -> f32 {
        match self {
            IconSize::S14 => 14.0,
            IconSize::S16 => 16.0,
            IconSize::S18 => 18.0,
            IconSize::S20 => 20.0,
            IconSize::S24 => 24.0,
            IconSize::S32 => 32.0,
        }
    }
}
