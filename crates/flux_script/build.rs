fn main() {
    if std::env::var("CARGO_CFG_TARGET_ENV").as_deref() == Ok("msvc") {
        // Per-symbol /EXPORT flags ADD to the export table; a /DEF file would
        // replace rustc's own generated exports (Rust metadata + shared
        // generics) and break downstream linking.
        for sym in LUAU_C_SYMBOLS {
            println!("cargo:rustc-link-arg=/EXPORT:{sym}");
        }
    }
}
